char		*ft_strcat(char *s1, const char *s2)
{
	int		i_s1;
	int		i_s2;

	i_s1 = 0;
	while (s1[i_s1] != '\0')
		i_s1++;
	i_s2 = 0;
	while (s2[i_s2] != '\0')
	{
		s1[i_s1] = s2[i_s2];
		i_s1++;
		i_s2++;
	}
	s1[i_s1] = '\0';
	return (s1);
}
